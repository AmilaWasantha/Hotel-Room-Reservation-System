    private void combopaymentidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combopaymentidActionPerformed
        try {
            String id=combopaymentid.getSelectedItem().toString();
            CustomeDTO cust=QueryController.getAllDetails(id);
            textreservationid.setText(cust.getR_id());
            textguest1.setText(cust.getGuest_id());
            textroomid.setText(cust.getRommId());
            textdate.setText(cust.getDate());
            textfullamount.setText(Double.toString(cust.getAmount()));
            textadvance.setText(Double.toString(cust.getAdvance()));
        } catch (Exception ex) {
            Logger.getLogger(ViewFullPaymentandPrintBill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_combopaymentidActionPerformed

    private void textadvanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textadvanceActionPerformed
        double amount=Double.parseDouble(textfullamount.getText());
        double addvance=Double.parseDouble(textadvance.getText());
      double arreas=amount-addvance;
      Text_Arrears.setText(""+arreas);
    }//GEN-LAST:event_textadvanceActionPerformed


