    private void loadPaymentId() {
        
       
        try {
            ArrayList<PaymentDTO>payment=PaymentController.getPaymentIds();
            for (PaymentDTO pay : payment) {
              combopaymentid.addItem(pay.getPayment_Id());
                
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewFullPaymentandPrintBill.class.getName()).log(Level.SEVERE, null, ex);
        }
            }

    private void loadTime() {
         Calendar c=Calendar.getInstance();
            SimpleDateFormat df=new SimpleDateFormat("hh.mm");
            String time=df.format(c.getTime());
            Text_time.setText(time);
    }
       
    
