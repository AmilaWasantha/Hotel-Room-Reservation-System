    private void btn_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditActionPerformed
        try {
            RoomDTO room=new RoomDTO(Double.parseDouble(Price_Text.getText()),combo_roomId.getSelectedItem()+"",RoomName_Text.getText());
            RoomTypeDTO roomType=new RoomTypeDTO(roomTypeId_Text.getText(),rommTypeText.getText(),description_Text.getText());
            
            boolean result=RoomMidifyController.Update(room,roomType);
            System.out.println(result);
            if(result){
                JOptionPane.showMessageDialog(this,"Update");
                clearAll();
            }else{
               
                JOptionPane.showMessageDialog(this," not Update");
                clearAll();
            }
        } catch (IOException ex) {
            Logger.getLogger(ModifyRooms.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_EditActionPerformed

