    private void combo_roomIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_roomIdActionPerformed
        try {
            String id=combo_roomId.getSelectedItem().toString();
            CustomeDTO room=QueryController.getAllroomDetail(id);
            roomTypeId_Text.setText(room.getRoomType_Id());
            rommTypeText.setText(room.getRoomType());
            description_Text.setText(room.getDescription());
            RoomName_Text.setText(room.getRoom_Name());
         
           
            Price_Text.setText(Double.toString(room.getPrice()));
            
        } catch (Exception ex) {
            Logger.getLogger(ModifyRooms.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_combo_roomIdActionPerformed

