    private void loadroomId() {
        
        try {
            ArrayList<String>room=RoomController.getRoomIds();
            
            for (String id : room) {
                combo_roomId.addItem(id);
                
            }
        } catch (Exception ex) {
            Logger.getLogger(ModifyRooms.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

